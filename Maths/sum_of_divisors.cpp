    Mint ans = 0;
    //count sum of divisors of all numbers from 1 to n
    ll i = 1;
    while(i <= n){
        ll q = n/i;
        ll j = n/q + 1;
        ans += (q*(q-1)/2)*(j-i);
        
        i = j+1;
    }
